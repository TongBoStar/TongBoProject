package com.terrence.crm.common.exception;

/**
 * Http传输异常.
 * 
 * @author harry.zu
 * 
 */
public final class HttpTransportException extends RuntimeException {
	private static final long serialVersionUID = 1L;

	/**
	 * 构造方法.
	 */
	public HttpTransportException() {
		super();
	}

	/**
	 * 构造方法.
	 * 
	 * @param msg
	 *            异常消息
	 */
	public HttpTransportException(final String msg) {
		super(msg);
	}
}
