package com.terrence.crm.dol.base;



/**
 * 基础查询类
 */
public class BaseQuery  implements java.io.Serializable {
	
	/** The sort columns. */
	private String sortColumns;
	
	/** The offset. */
	private long offset;
	
	/** The page size. */
	private long pageSize;
	
	/** The last rows. */
	private long lastRows;
	/**select top **/
	private String top;
	/***not in (select top )**/
	private String notTop;
	/**primary key**/
	private String priColumn;
	/**
	 * Gets the sort columns.
	 *
	 * @return the sort columns
	 */
	public String getSortColumns() {
		return sortColumns;
	}
	
	/**
	 * Sets the sort columns.
	 *
	 * @param sortColumns the new sort columns
	 */
	public void setSortColumns(String sortColumns) {
		this.sortColumns = sortColumns;
	}
	
	/**
	 * Gets the offset.
	 *
	 * @return the offset
	 */
	public long getOffset() {
		return offset;
	}
	
	/**
	 * Sets the offset.
	 *
	 * @param offset the new offset
	 */
	public void setOffset(long offset) {
		this.offset = offset;
	}
	
	/**
	 * Gets the page size.
	 *
	 * @return the page size
	 */
	public long getPageSize() {
		return pageSize;
	}
	
	/**
	 * Sets the page size.
	 *
	 * @param pageSize the new page size
	 */
	public void setPageSize(long pageSize) {
		this.pageSize = pageSize;
	}
	
	/**
	 * Gets the last rows.
	 *
	 * @return the last rows
	 */
	public long getLastRows() {
		return lastRows;
	}
	
	/**
	 * Sets the last rows.
	 *
	 * @param lastRows the new last rows
	 */
	public void setLastRows(long lastRows) {
		this.lastRows = lastRows;
	}

	public String getNotTop() {
		return notTop;
	}

	public void setNotTop(String notTop) {
		this.notTop = notTop;
	}

	public String getTop() {
		return top;
	}

	public void setTop(String top) {
		this.top = top;
	}

	public String getPriColumn() {
		return priColumn;
	}

	public void setPriColumn(String priColumn) {
		this.priColumn = priColumn;
	}
	
	
	  
}
